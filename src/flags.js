export function getFlagEmoji(countryCode) {
  if (countryCode === "G001") {
    return "🌏";
  }

  const iso2Code = imfCountryCodeToIso2[countryCode];

  if (!iso2Code) {
    return "";
  }

  return iso2Code
    .toUpperCase()
    .split("")
    .map((letter) => String.fromCodePoint(127397 + letter.charCodeAt(0)))
    .join("");
}
